def overlap(r1, r2):
	"""
	Returns if the two ranges are overlapping. Notes, range should be given in order

	Examples:
	>>> overlap((2,3), (5,10))
	False
	>>> overlap((2,3), (1,10))
	True
	>>> overlap((5,15), (3,8))
	True
	>>> overlap((3,7), (5,9))
	True
	"""
	index = (r2[0] <= r1[0] <= r2[1]) or (r1[0] <= r2[0] <= r1[1])
	return index
